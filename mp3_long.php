          <li class="talk"><p>For the purpose of putting an end to suffering, the Buddha divided questions into four types: those that deserve a categorical answer, those that deserve an analytical answer, those that deserve that the person asking the question be cross-questioned, and those that deserve to be put aside. To know where a particular question falls within these categories, he said, is a sign of discernment. This day-long course looked at how the Buddha himself applied this scheme to the questions that other people asked him and to the questions he asked himself during his search for awakening. The purpose is be to gain a deeper understanding of the Buddha’s teachings by seeing what kinds of questions they were and were not intended to answer, and to gain a better sense of what kinds of questions we should be asking ourselves to guide our practice.</p></li>
        </ul>
